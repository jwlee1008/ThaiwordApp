def parse_items(text: str) -> list[str]:
    items: list[str] = []
    for line in text.splitlines():
        value = line.strip()
        if value and not value.startswith("#"):
            items.append(value)
    return items


def append_unique(path: Path, addition_text: str) -> tuple[int, int]:
    existing_text = path.read_text(encoding="utf-8")
    seen = set(parse_items(existing_text))
    added = 0
    output_lines = [existing_text.rstrip(), "", "# expanded curated queries"]

    for line in addition_text.strip().splitlines():
        value = line.strip()
        if not value:
            output_lines.append("")
            continue
        if value.startswith("#"):
            output_lines.append(value)
            continue
        if value in seen:
            continue
        seen.add(value)
        added += 1
        output_lines.append(value)

    path.write_text("\n".join(output_lines).rstrip() + "\n", encoding="utf-8")
    return added, len(seen)


def main() -> int:
    for level, path in FILES.items():
        combined = ADDITIONS[level] + "\n" + MORE_ADDITIONS[level]
        added, total = append_unique(path, combined)
        print(f"{level}: added {added}, total {total}")
    return 0


