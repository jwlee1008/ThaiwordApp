def append_unique(path: Path, text: str) -> tuple[int, int]:
    existing = path.read_text(encoding="utf-8")
    seen = {
        line.strip()
        for line in existing.splitlines()
        if line.strip() and not line.strip().startswith("#")
    }
    output = [existing.rstrip(), "", "# expanded curated queries v3"]
    added = 0
    for line in text.strip().splitlines():
        value = line.strip()
        if not value:
            output.append("")
            continue
        if value.startswith("#"):
            output.append(value)
            continue
        if value in seen:
            continue
        seen.add(value)
        added += 1
        output.append(value)
    path.write_text("\n".join(output).rstrip() + "\n", encoding="utf-8")
    return added, len(seen)


def main() -> int:
    for level, path in FILES.items():
        added, total = append_unique(path, ADDITIONS[level])
        print(f"{level}: added {added}, total {total}")
    return 0


